# PARTE 1: DECLARACIÓN DE VARIABLES
print("=== VARIABLES Y TIPOS DE DATOS ===")

# Variables numéricas
edad = 17
altura = 1.75
peso = 65.5

# Variables de texto
nombre = "María"
apellido = "González"
escuela = "CBTIS"

# Variables booleanas
es_estudiante = True
tiene_beca = False

# PARTE 2: MOSTRAR INFORMACIÓN
print(f"Nombre completo: {nombre} {apellido}")
print(f"Edad: {edad} años")
print(f"Altura: {altura} metros")
print(f"Peso: {peso} kg")
print(f"Escuela: {escuela}")
print(f"¿Es estudiante?: {es_estudiante}")
print(f"¿Tiene beca?: {tiene_beca}")

# PARTE 3: VERIFICAR TIPOS DE DATOS
print("\n=== TIPOS DE DATOS ===")
print(f"Tipo de edad: {type(edad)}")
print(f"Tipo de altura: {type(altura)}")
print(f"Tipo de nombre: {type(nombre)}")
print(f"Tipo de es_estudiante: {type(es_estudiante)}")

